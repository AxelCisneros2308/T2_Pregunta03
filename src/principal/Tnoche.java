    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if(listaEstudiantes.isEmpty()){
            JOptionPane.showMessageDialog(this, "No hay estudiantes para eliminar");
            return;
        }
        
        int index = lstEstudiantes.getSelectedIndex();
        if(index == -1){
            JOptionPane.showMessageDialog(this, "Seleccione un Estudiante");
            return;
        }
        
        estudiantes.remove(index);
        listaEstudiantes.remove(index);
    }//GEN-LAST:event_jButton2ActionPerformed

